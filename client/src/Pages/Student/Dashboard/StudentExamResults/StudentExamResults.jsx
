import React, { useState, useEffect } from 'react';
import { useNavigate } from 'react-router-dom';
import {
    ArrowLeft,
    Download,
    Search,
    Clock,
    CheckCircle,
    FileText,
    BarChart3,
    Award,
    AlertCircle
} from 'lucide-react';
import { getMySubmissions, downloadAnswerFile } from '../../../../lib/api/examApi';

const StudentExamResults = () => {
    const navigate = useNavigate();
    const [submissions, setSubmissions] = useState([]);
    const [loading, setLoading] = useState(true);
    const [error, setError] = useState(null);
    const [filter, setFilter] = useState('all');
    const [searchTerm, setSearchTerm] = useState('');

    useEffect(() => {
        fetchSubmissions();
    }, []);

    const fetchSubmissions = async () => {
        try {
            setLoading(true);
            const res = await getMySubmissions();
            if (res.success) {
                setSubmissions(res.submissions);
            }
        } catch (err) {
            setError('Failed to load your exam submissions');
            console.error(err);
        } finally {
            setLoading(false);
        }
    };

    const formatDate = (date) => {
        if (!date) return '-';
        return new Date(date).toLocaleDateString('en-US', {
            year: 'numeric',
            month: 'short',
            day: 'numeric',
            hour: '2-digit',
            minute: '2-digit'
        });
    };

    const handleDownloadAnswer = async (submissionId, fileName) => {
        try {
            const blob = await downloadAnswerFile(submissionId);
            const url = window.URL.createObjectURL(blob);
            const a = document.createElement('a');
            a.href = url;
            a.download = fileName || 'your_answer.pdf';
            document.body.appendChild(a);
            a.click();
            window.URL.revokeObjectURL(url);
            document.body.removeChild(a);
        } catch (err) {
            console.error('Failed to download answer file:', err);
            alert('Failed to download your answer file');
        }
    };

    const getStatusColor = (status, gradingStatus) => {
        if (gradingStatus === 'published') return 'bg-green-100 text-green-700';
        if (gradingStatus === 'graded') return 'bg-blue-100 text-blue-700';
        if (gradingStatus === 'pending') return 'bg-yellow-100 text-yellow-700';
        return 'bg-gray-100 text-gray-700';
    };

    const getStatusText = (gradingStatus) => {
        switch (gradingStatus) {
            case 'published': return 'Result Published';
            case 'graded': return 'Graded';
            case 'pending': return 'Pending';
            default: return 'Unknown';
        }
    };

    const filteredSubmissions = submissions.filter(sub => {
        const matchesFilter = filter === 'all' || 
            (filter === 'published' && sub.gradingStatus === 'published') ||
            (filter === 'graded' && sub.gradingStatus === 'graded') ||
            (filter === 'pending' && sub.gradingStatus === 'pending');
        
        const matchesSearch = sub.exam?.title?.toLowerCase().includes(searchTerm.toLowerCase()) ||
            sub.course?.title?.toLowerCase().includes(searchTerm.toLowerCase());
        
        return matchesFilter && matchesSearch;
    }) || [];

    const stats = {
        total: submissions.length,
        published: submissions.filter(s => s.gradingStatus === 'published').length,
        graded: submissions.filter(s => s.gradingStatus === 'graded').length,
        pending: submissions.filter(s => s.gradingStatus === 'pending').length
    };

    if (loading) {
        return (
            <div className="min-h-screen bg-gradient-to-br from-blue-50 to-indigo-100 flex items-center justify-center">
                <div className="animate-spin rounded-full h-16 w-16 border-b-4 border-indigo-600"></div>
            </div>
        );
    }

    return (
        <div className="min-h-screen bg-gradient-to-br from-blue-50 to-indigo-100 p-4 md:p-8">
            <div className="max-w-7xl mx-auto">
                {/* Header */}
                <div className="mb-8">
                    <button
                        onClick={() => navigate('/student-dashboard')}
                        className="flex items-center gap-2 text-indigo-600 hover:text-indigo-700 mb-4"
                    >
                        <ArrowLeft className="w-5 h-5" />
                        Back to Dashboard
                    </button>
                    
                    <div className="flex flex-col md:flex-row md:items-center justify-between gap-4">
                        <div>
                            <h1 className="text-3xl font-bold text-gray-800 flex items-center gap-3">
                                <Award className="w-8 h-8 text-indigo-600" />
                                My Exam Results
                            </h1>
                            <p className="text-gray-600 mt-1">View your exam submissions and results</p>
                        </div>
                    </div>
                </div>

                {/* Stats Cards */}
                <div className="grid grid-cols-2 md:grid-cols-4 gap-4 mb-8">
                    <div className="bg-white rounded-xl shadow-lg p-4">
                        <div className="flex items-center gap-3">
                            <div className="p-2 bg-blue-100 rounded-lg">
                                <FileText className="w-5 h-5 text-blue-600" />
                            </div>
                            <div>
                                <p className="text-2xl font-bold text-gray-800">{stats.total}</p>
                                <p className="text-sm text-gray-500">Total Submissions</p>
                            </div>
                        </div>
                    </div>

                    <div className="bg-white rounded-xl shadow-lg p-4">
                        <div className="flex items-center gap-3">
                            <div className="p-2 bg-green-100 rounded-lg">
                                <CheckCircle className="w-5 h-5 text-green-600" />
                            </div>
                            <div>
                                <p className="text-2xl font-bold text-gray-800">{stats.published}</p>
                                <p className="text-sm text-gray-500">Published</p>
                            </div>
                        </div>
                    </div>

                    <div className="bg-white rounded-xl shadow-lg p-4">
                        <div className="flex items-center gap-3">
                            <div className="p-2 bg-purple-100 rounded-lg">
                                <BarChart3 className="w-5 h-5 text-purple-600" />
                            </div>
                            <div>
                                <p className="text-2xl font-bold text-gray-800">{stats.graded}</p>
                                <p className="text-sm text-gray-500">Graded</p>
                            </div>
                        </div>
                    </div>

                    <div className="bg-white rounded-xl shadow-lg p-4">
                        <div className="flex items-center gap-3">
                            <div className="p-2 bg-yellow-100 rounded-lg">
                                <Clock className="w-5 h-5 text-yellow-600" />
                            </div>
                            <div>
                                <p className="text-2xl font-bold text-gray-800">{stats.pending}</p>
                                <p className="text-sm text-gray-500">Pending</p>
                            </div>
                        </div>
                    </div>
                </div>

                {/* Filters */}
                <div className="bg-white rounded-xl shadow-lg p-4 mb-6">
                    <div className="flex flex-col md:flex-row gap-4">
                        <div className="flex-1 relative">
                            <Search className="absolute left-3 top-1/2 transform -translate-y-1/2 w-5 h-5 text-gray-400" />
                            <input
                                type="text"
                                placeholder="Search by exam or course name..."
                                value={searchTerm}
                                onChange={(e) => setSearchTerm(e.target.value)}
                                className="w-full pl-10 pr-4 py-2 border border-gray-300 rounded-lg focus:ring-2 focus:ring-indigo-500 focus:border-indigo-500"
                            />
                        </div>
                        <div className="flex gap-2">
                            <select
                                value={filter}
                                onChange={(e) => setFilter(e.target.value)}
                                className="px-4 py-2 border border-gray-300 rounded-lg focus:ring-2 focus:ring-indigo-500 focus:border-indigo-500"
                            >
                                <option value="all">All Results</option>
                                <option value="published">Published</option>
                                <option value="graded">Graded</option>
                                <option value="pending">Pending</option>
                            </select>
                        </div>
                    </div>
                </div>

                {/* Results Table */}
                <div className="bg-white rounded-xl shadow-lg overflow-hidden">
                    <div className="overflow-x-auto">
                        <table className="w-full">
                            <thead className="bg-gray-50">
                                <tr>
                                    <th className="px-6 py-3 text-left text-xs font-medium text-gray-500 uppercase tracking-wider">
                                        Exam
                                    </th>
                                    <th className="px-6 py-3 text-left text-xs font-medium text-gray-500 uppercase tracking-wider">
                                        Course
                                    </th>
                                    <th className="px-6 py-3 text-left text-xs font-medium text-gray-500 uppercase tracking-wider">
                                        Status
                                    </th>
                                    <th className="px-6 py-3 text-left text-xs font-medium text-gray-500 uppercase tracking-wider">
                                        Score
                                    </th>
                                    <th className="px-6 py-3 text-left text-xs font-medium text-gray-500 uppercase tracking-wider">
                                        Submitted
                                    </th>
                                    <th className="px-6 py-3 text-left text-xs font-medium text-gray-500 uppercase tracking-wider">
                                        Actions
                                    </th>
                                </tr>
                            </thead>
                            <tbody className="divide-y divide-gray-200">
                                {filteredSubmissions.map((submission) => (
                                    <tr key={submission.id} className="hover:bg-gray-50">
                                        <td className="px-6 py-4">
                                            <div>
                                                <p className="font-medium text-gray-800">
                                                    {submission.exam?.title || 'Unknown Exam'}
                                                </p>
                                                <p className="text-sm text-gray-500">
                                                    Total Marks: {submission.totalMarks}
                                                </p>
                                            </div>
                                        </td>
                                        <td className="px-6 py-4">
                                            <p className="text-gray-600">
                                                {submission.course?.title || '-'}
                                            </p>
                                        </td>
                                        <td className="px-6 py-4">
                                            <span className={`inline-flex items-center gap-1 px-3 py-1 rounded-full text-xs font-medium ${getStatusColor(submission.status, submission.gradingStatus)}`}>
                                                {getStatusText(submission.gradingStatus)}
                                            </span>
                                        </td>
                                        <td className="px-6 py-4">
                                            {submission.gradingStatus !== 'pending' ? (
                                                <div className="flex items-center gap-2">
                                                    <span className={`font-bold ${submission.passed ? 'text-green-600' : 'text-red-600'}`}>
                                                        {submission.obtainedMarks}/{submission.totalMarks}
                                                    </span>
                                                    <span className="text-sm text-gray-500">
                                                        ({submission.percentage?.toFixed(1)}%)
                                                    </span>
                                                </div>
                                            ) : (
                                                <span className="text-gray-400">-</span>
                                            )}
                                        </td>
                                        <td className="px-6 py-4 text-sm text-gray-500">
                                            {formatDate(submission.submittedAt)}
                                        </td>
                                        <td className="px-6 py-4">
                                            <div className="flex gap-2">
                                                {/* Download my answer */}
                                                <button
                                                    onClick={() => handleDownloadAnswer(submission.id, `answer_${submission.exam?.title}.pdf`)}
                                                    className="flex items-center gap-1 px-3 py-1 text-blue-600 hover:bg-blue-50 rounded-lg text-sm font-medium"
                                                >
                                                    <Download className="w-4 h-4" />
                                                    My Answer
                                                </button>
                                                
                                                {/* Show result if published */}
                                                {submission.gradingStatus === 'published' && (
                                                    <div className="flex items-center gap-1 px-3 py-1 text-green-600 bg-green-50 rounded-lg text-sm font-medium">
                                                        <CheckCircle className="w-4 h-4" />
                                                        View Result
                                                    </div>
                                                )}
                                                
                                                {submission.gradingStatus === 'graded' && submission.gradingStatus !== 'published' && (
                                                    <div className="flex items-center gap-1 px-3 py-1 text-yellow-600 bg-yellow-50 rounded-lg text-sm font-medium">
                                                        <Clock className="w-4 h-4" />
                                                        Awaiting
                                                    </div>
                                                )}
                                            </div>
                                        </td>
                                    </tr>
                                ))}
                            </tbody>
                        </table>
                    </div>

                    {filteredSubmissions.length === 0 && (
                        <div className="text-center py-12">
                            <FileText className="w-16 h-16 text-gray-300 mx-auto mb-4" />
                            <p className="text-gray-600">No exam results found</p>
                        </div>
                    )}
                </div>

                {/* Help Text */}
                <div className="mt-6 bg-blue-50 border border-blue-200 rounded-xl p-4">
                    <div className="flex items-start gap-3">
                        <AlertCircle className="w-5 h-5 text-blue-600 mt-0.5" />
                        <div>
                            <h3 className="font-semibold text-blue-800">Understanding Your Results</h3>
                            <ul className="text-sm text-blue-700 mt-2 space-y-1">
                                <li>• <strong>Pending:</strong> Your exam has been submitted and is awaiting evaluation</li>
                                <li>• <strong>Graded:</strong> Your exam has been graded but results are not yet published</li>
                                <li>• <strong>Published:</strong> Your results are now visible to you</li>
                            </ul>
                        </div>
                    </div>
                </div>
            </div>
        </div>
    );
};

export default StudentExamResults;

